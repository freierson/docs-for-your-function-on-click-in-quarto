#' Add Together Two Numbers
#'
#' @description Calculates the sum \eqn{x+y}.
#'
#' @param x A number.
#' @param y A number.
#' @return A number.
#' @examples
#' add(1, 1)
#' add(10, 1)
#' @export
add <- function(x, y) {
  x + y
}

#' Multiply Together Two Numbers
#'
#' @description Calculates the product \eqn{x\times y}.
#' 
#' @param x A number.
#' @param y A number.
#'
#' @returns A number.
#' @examples
#' multiply(2,3)
#' multiply(7,10)
#' @export
multiply <- function(x, y) {
  x * y
}